
public class EmInfo {
	 static String ename;
	  static int eno;
	  static double gross, net;
	  public void setEno(int no)
	  { eno = no;
	  }  
	  public int getEno()
	  { return eno;
	  }
	  public void setEname(String na)
	  { ename = na;
	  }  
	  public String getEname()
	  { return ename;
	  }
	  public void setGross(double g)
	  { gross = g;
	  }  
	  public double getGross()
	  { return gross;
	  }
	 public void setNet(double n)
	  { net = n;
	  }  
	  public double getNet()
	  { return net;
	  }
}
